const enrichImage = (baseUrl, signature) => {
  const separator = baseUrl.includes('?') ? '&' : '?';
  return `${baseUrl}${separator}auto=format&fit=crop&w=1200&q=85&sat=-5&sig=${signature}`;
};

const buildProductCatalog = () =>
  catalogBase.flatMap(({ category, items }) =>
    items.flatMap((item, itemIdx) =>
      variantTiers.map((variant, variantIdx) => ({
        name: `${item.name} ${variant.suffix}`,
        description: item.description,
        price: Number((item.price + variant.priceBump).toFixed(2)),
        category,
        images: [enrichImage(item.image, itemIdx * 10 + variantIdx + 1)],
        brand: item.brand,
        stock: item.stock,
        rating: item.rating,
        reviewsNumber: item.reviewsNumber + variantIdx * 15,
        sales: item.sales + variantIdx * 8,
      }))
    )
  );

const seedProducts = buildProductCatalog();

module.exports = { seedProducts };
